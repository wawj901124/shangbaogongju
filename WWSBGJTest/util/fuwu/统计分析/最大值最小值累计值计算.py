def choose(s):
    sum = 0
    all = 0
    maxnum = max(s)
    minnum = min(s)
    for i in s:
        sum = sum + 1 #元素个数
        all = all + i
    average = all / sum
    print(str("元素个数{0}，最大值{1}，最小值{2}，元素和{3}，平均值{4}").format(sum, maxnum, minnum, all, average))
